object BusStop extends Enumeration {
    type BusStop = BusStopType

    case class BusStopType(val code:String, val name:String) extends Val(code)

    def getBusStopByCode(code:String):BusStopType = {
        return BusStop.withName(code).asInstanceOf[BusStop.BusStopType]
    }

    def getBusStopByName(name:String):BusStopType = {
        for(busStop <- BusStop.values) {
            val bs = busStop.asInstanceOf[BusStopType]

            if(name == bs.name) {
                return bs
            }
        }

        return null
    }

    def names:SortedSet[String] = {
        for(busStop <- BusStop.values) yield busStop.asInstanceOf[BusStopType].name
    }

    protected final def BusStopValue(code:String, name:String):BusStopType = {
        return BusStopType(code, name)
    }

